def classify_region(city_input, gu_input):
    """
    분리되어 입력받은 시(도)와 구(군) 데이터를 전국 지역 DB와 대조하여
    수도권/비수도권 여부를 판별하고, 오타 및 존재하지 않는 지역 조합을 차단합니다.
    """
    city = city_input.replace(" ", "")
    gu = gu_input.replace(" ", "")
    
    # 1. 명백한 오타 사전 감지
    known_typos = ["서을", "인쳔", "경가도"]
    if any(t in city + gu for t in known_typos):
        return "TYPO"

    # 2. 전국 광역시/도 및 하위 시/구/군 데이터베이스 (정확도 100%를 위한 교차 검증용)
    region_db = {
        ("서울", "서울특별시"): ["강남", "강동", "강북", "강서", "관악", "광진", "구로", "금천", "노원", "도봉", "동대문", "동작", "마포", "서대문", "서초", "성동", "성북", "송파", "양천", "영등포", "용산", "은평", "종로", "중구", "중랑"],
        ("인천", "인천광역시", "인천시"): ["계양", "미추홀", "남동", "동구", "부평", "서구", "연수", "중구", "강화", "옹진", "웅진"],
        ("경기", "경기도"): ["수원", "장안", "권선", "팔달", "영통", "성남", "수정", "중원", "분당", "안양", "만안", "동안", "안산", "상록", "단원", "고양", "덕양", "일산", "용인", "처인", "기흥", "수지", "부천", "원미", "소사", "오정", "광명", "평택", "과천", "오산", "시흥", "군포", "의왕", "하남", "이천", "안성", "김포", "화성", "광주", "여주", "양주", "포천", "동두천", "가평", "연천"],
        ("부산", "부산광역시", "부산시"): ["중구", "서구", "동구", "영도", "부산진", "동래", "남구", "북구", "해운대", "사하", "금정", "강서", "연제", "수영", "사상", "기장"],
        ("대구", "대구광역시", "대구시"): ["중구", "동구", "서구", "남구", "북구", "수성", "달서", "달성", "군위"],
        ("광주", "광주광역시", "광주시"): ["동구", "서구", "남구", "북구", "광산"],
        ("대전", "대전광역시", "대전시"): ["동구", "중구", "서구", "유성", "대덕"],
        ("울산", "울산광역시", "울산시"): ["중구", "남구", "동구", "북구", "울주"],
        ("세종", "세종특별자치시", "세종시"): ["세종", "조치원", "전의", "전동", "소정", "연서", "연동", "연기", "금남", "장군", "부강", "읍", "면", "동"],
        ("강원", "강원도", "강원특별자치도"): ["춘천", "원주", "강릉", "동해", "태백", "속초", "삼척", "홍천", "횡성", "영월", "평창", "정선", "철원", "화천", "양구", "인제", "고성", "양양"],
        ("충북", "충청북도"): ["청주", "상당", "서원", "흥덕", "청원", "충주", "제천", "보은", "옥천", "영동", "증평", "진천", "괴산", "음성", "단양"],
        ("충남", "충청남도"): ["천안", "동남", "서북", "공주", "보령", "아산", "서산", "논산", "계룡", "당진", "금산", "부여", "서천", "청양", "홍성", "예산", "태안"],
        ("전북", "전라북도", "전북특별자치도"): ["전주", "완산", "덕진", "군산", "익산", "정읍", "남원", "김제", "완주", "진안", "무주", "장수", "임실", "순창", "고창", "부안"],
        ("전남", "전라남도"): ["목포", "여수", "순천", "나주", "광양", "담양", "곡성", "구례", "고흥", "보성", "화순", "장흥", "강진", "해남", "영암", "무안", "함평", "영광", "장성", "완도", "진도", "신안"],
        ("경북", "경상북도"): ["포항", "남구", "북구", "경주", "김천", "안동", "구미", "영주", "영천", "상주", "문경", "경산", "의성", "청송", "영양", "영덕", "청도", "고령", "성주", "칠곡", "예천", "봉화", "울진", "울릉"],
        ("경남", "경상남도"): ["창원", "의창", "성산", "합포", "회원", "진해", "진주", "통영", "사천", "김해", "밀양", "거제", "양산", "의령", "함안", "창녕", "고성", "남해", "하동", "산청", "함양", "거창", "합천"],
        ("제주", "제주도", "제주특별자치도"): ["제주", "서귀포"]
    }

    # 3. 입력한 시(도)가 DB에 존재하는지 확인
    matched_city_key = None
    matched_gus = []
    
    for aliases, gus in region_db.items():
        if any(alias in city for alias in aliases):
            matched_city_key = aliases[0] # 대표 이름 (예: '서울', '제주')
            matched_gus = gus
            break
            
    if not matched_city_key:
        return "INVALID_CITY" # 존재하지 않는 시(도)를 입력함

    # 4. 입력한 구(군)이 해당 시(도)에 실존하는지 꼼꼼히 교차 검증 (오류 원천 차단)
    if gu:
        if matched_city_key != "세종": # 세종시는 구가 없으므로 제외
            is_valid_gu = False
            for valid_gu in matched_gus:
                if valid_gu in gu:
                    is_valid_gu = True
                    break
            
            if not is_valid_gu:
                return "INVALID_GU" # (예: 제주도에 은평구를 입력한 경우 여기서 걸러짐)

    # 5. 수도권 vs 비수도권 최종 분류
    if matched_city_key in ["서울", "인천", "경기"]:
        # 인천/경기는 예외 지역이 있으므로 구(군) 정보를 무조건 받아야 함
        if matched_city_key in ["인천", "경기"] and not gu:
            return "REQUIRE_GU"
            
        # [예외] 인천/경기 내 인구감소지역 (비수도권으로 분류)
        exception_gus = ["강화", "옹진", "웅진", "가평", "연천"]
        if any(e in gu for e in exception_gus):
            return "비수도권 (인천/경기 내 인구감소지역 예외)"
            
        return "수도권"
    else:
        # 그 외 지역(부산, 제주 등)은 모두 비수도권
        return "비수도권"



if __name__ == "__main__":
    print("=" * 65)
    print(" 청년일자리도약장려금 지역 분류 프로그램 (최종 완성판)")
    print("=" * 65)

    while True:
        print("\n" + "-" * 50)
        
        # 1단계: 시(도) 입력
        city_input = input("1. 시(도)를 입력해주세요. (종료: q) : ")
        
        if city_input.strip().lower() in ["q", "종료", "exit"]:
            print("프로그램을 종료합니다.")
            break
            
        if not city_input.strip():
            print("▶ ⚠️ 알림: 시(도)를 반드시 입력해야 합니다.")
            continue
            
        # 2단계: 구(군) 입력 (제주도 등 섬 지역 입력 시 명령어 자동 변경)
        if "제주" in city_input:
            prompt_msg = "2. 시(구/군)을 입력하세요.           : "
        else:
            prompt_msg = "2. 구(군)을 입력해주세요.           : "
            
        gu_input = input(prompt_msg)
        
        # 분류 알고리즘 호출
        result = classify_region(city_input, gu_input)
        
        # ---------------- 결과 및 오류 메시지 출력 ----------------
        display_region = f"{city_input.strip()} {gu_input.strip()}".strip()

        if result == "INVALID_CITY":
            print("▶ ⚠️ 알림: 시(도) 이름이 올바르지 않습니다. (예: 서울, 경기, 경남, 제주 등으로 정확히 입력)")
        elif result == "INVALID_GU":
            print(f"▶ ⚠️ 알림: '{city_input}'에는 입력하신 '{gu_input}'(이)가 존재하지 않거나 오타입니다.")
        elif result == "REQUIRE_GU":
            print(f"▶ ⚠️ 알림: '{city_input}' 지역은 상세 구분에 따라 예외가 있으므로, 2번 항목을 반드시 입력해야 합니다.")
        elif result == "TYPO":
            print("▶ ⚠️ 알림: 오타가 발생했습니다. 확인 후 다시 입력해주세요.")
        else:
            print(f"▶ 결과: [{display_region}] 지역은 '{result}'입니다.")